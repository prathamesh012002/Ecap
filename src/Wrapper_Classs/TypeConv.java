package Wrapper_Classs;

public class TypeConv {

	public static void main(String[] args) {
		int a = 10;
		double b=20.23;
		char c='a';
		boolean bl=true;
		String s=String.valueOf(bl);
		System.out.println(s);
		s=String.valueOf(a);
		System.out.println(a);
		s=String.valueOf(c);
		System.out.println(c);
		s=String.valueOf(b);
		System.out.println(b);
		
	}

}
